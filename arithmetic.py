# arithmatic
#             + Addition
#             - Subtraction
#             * Multiplication
#             / Division
#             // Integer Division (Return an integer)
#             %  Remainder

#Initially
friends = 5
print(f"Initial === current value is: {friends}")
print(friends)

# Addition
print(f"Addition === current value is: {friends}")
friends = friends + 5
friends += 5
print(friends)

#Subtraction
print(f"Subtraction === current value is: {friends}")
friends = friends - 5
friends -= 5
print(friends)

#Division (Float)
print(f"Division (Float) === current value is: {friends}")
friends = friends / 2
friends /= 2
print(friends)

friends = 100
#Division (Round version)
print(f"Division (Round version) === current value is: {friends}")
friends = friends // 2
friends //= 2
print(friends)

#Multiplication
print(f"Multiplication === current value is: {friends}")
friends = friends * 2
friends *= 2
print(friends)

#Modulus
print(f"Modulus === current value is: {friends}")
friends = friends % 40
friends %= 3
print(friends)